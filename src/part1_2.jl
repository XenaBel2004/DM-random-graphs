# ╔═╡ 253c1bba-350c-4d9f-a645-41dab39cdb6f
begin
#не импортировалась функция хром.числа, поэтому жадная раскраска
    function greedy_coloring(g::SimpleGraph)
        n = nv(g)
        colors = zeros(Int, n)
        for v in 1:n
            used = Set(colors[u] for u in neighbors(g, v) if colors[u] != 0)
            c = 1
            while c in used
                c += 1
            end
            colors[v] = c
        end
        return maximum(colors)
    end
#функция подсчёта характеристик
    function graph_metrics(g::SimpleGraph)
        δ = minimum(degree(g))
        χ = greedy_coloring(g)
        return δ, χ
    end
end

# ╔═╡ ac8bc6a5-4026-47f3-999c-0ac5fa5cbfb5
#построение knn графа
begin
	function build_knn_graph(data::Vector{<:Real}, k::Int)
	        n = length(data)
	        g = SimpleGraph(n)
	        for i in 1:n
	            distances = [(j, abs(data[i] - data[j])) for j in 1:n if j != i]
	            nearest = sort(distances, by = x -> x[2])[1:k]
	            for (j, _) in nearest
	                add_edge!(g, i, j)
	            end
	        end
	        return g
	end
end

# ╔═╡ 80d645df-2514-49d9-9446-41f9d1298cd5
#построение dist графа
begin
function build_distance_graph(data, d)
	      n = length(data)
	      g = SimpleGraph(n)
	      for i in 1:n-1
	          for j in i+1:n
	              if abs(data[i] - data[j]) ≤ d
	                  add_edge!(g, i, j)
	              end
	          end
	      end
	      return g
	  end
end

